    private void configurarTabela() {

        tblListaImoveis.setRowSelectionAllowed(true);
        tblListaImoveis.setColumnSelectionAllowed(false);
        tblListaImoveis.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        tblListaImoveis.addMouseListener(new java.awt.event.MouseAdapter() {

            @Override
            public void mousePressed(java.awt.event.MouseEvent evt) {

                int row = tblListaImoveis.rowAtPoint(evt.getPoint());

                if (row == -1) {
                    return;
                }

                tblListaImoveis.setRowSelectionInterval(row, row);

                if (evt.getClickCount() == 2
                        && SwingUtilities.isLeftMouseButton(evt)) {

                    System.out.println("DUPLO CLIQUE OK");
                    abrirImovelSelecionado();
                }
            }
        });
    }

