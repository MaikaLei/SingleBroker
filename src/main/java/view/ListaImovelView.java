    private void carregarTabela() {

        ImovelDao dao = new ImovelDao();

        DefaultTableModel modelo
                = (DefaultTableModel) tblListaImoveis.getModel();

        modelo.setRowCount(0);

        List<ImovelModel> lista = dao.listar();

        for (ImovelModel imovel : lista) {

            modelo.addRow(new Object[]{
                imovel.getId(),
                imovel.getStatusImovel(),
                imovel.getTransacao(),
                imovel.getTipoImovel(),
                imovel.getValor()

            });
        }
    }

    private void buscarImoveis() {

        ImovelDao dao = new ImovelDao();

        DefaultTableModel modelo
                = (DefaultTableModel) tblListaImoveis.getModel();

        modelo.setRowCount(0);

        List<ImovelModel> lista = dao.buscar(
                txtCodigoBusca.getText().trim(),
                cbxStatus.getSelectedItem().toString(),
                cbxTransacao.getSelectedItem().toString(),
                cbxTipo.getSelectedItem().toString(),
                cbxCidade.getSelectedItem().toString());

        for (ImovelModel imovel : lista) {

            modelo.addRow(new Object[]{
                imovel.getId(),
                imovel.getStatusImovel(),
                imovel.getTransacao(),
                imovel.getTipoImovel(),
                imovel.getValor()
            });
        }
    }

    private void limparFiltros() {

        txtCodigoBusca.setText("");

        cbxStatus.setSelectedIndex(0);

        cbxTransacao.setSelectedIndex(0);

        cbxTipo.setSelectedIndex(0);

        cbxCidade.setSelectedIndex(0);

        carregarTabela();
    }

    private void abrirImovelSelecionado() {

        int linha = tblListaImoveis.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um imóvel.");
            return;
        }

        Long id = (Long) tblListaImoveis.getValueAt(linha, 0);

        try {
            ImovelDao dao = new ImovelDao();
            ImovelModel imovel = dao.buscarPorId(id);

            if (imovel == null) {
                JOptionPane.showMessageDialog(this, "Imóvel não encontrado.");
                return;
            }

            ImovelView view = new ImovelView(imovel);

            view.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao abrir imóvel: " + e.getMessage());
            e.printStackTrace();
        }
    }

